list=[6,7,2,7]
list.append(1)
print(list,id(list))
list.insert(5,2)
print(list,id(list))
list.reverse()
print(list,id(list))
list.remove(1)
print(list,id(list))
list.pop()
print(list,id(list))
newlist=list.copy()
print(newlist,id(newlist))
list.clear()
print(list,id(list))
print(newlist,id(newlist))